public class Shift {
    public int id;
    public int number;
    public int numberOfTheLesson;
    public int startTime;
    public int endTime;

    public Shift(int id, int number, int numberOfTheLesson, int startTime, int endTime) {
        this.id = id;
        this.number = number;
        this.numberOfTheLesson = numberOfTheLesson;
        this.startTime = startTime;
        this.endTime = endTime;
    }
}
